class A 
{
	public static void main(String[] args) 
	{
		int i = 0;
		System.out.println(i++); // i = i + 1; //immediate value = 0
		System.out.println(i); // updated value = 1
		System.out.println(i); // updated value = 1

		// Unary Operators
		// 1. Increment
		// >> pre increment
		// >> post increment

		// 2. Decrement
		// >> pre decrement
		// >> post decrement
	}
}
